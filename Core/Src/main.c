/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

/* Configure the peripherals common clocks */
  PeriphCommonClock_Config();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_ADC1_Init();
  MX_ADC2_Init();
  MX_ADC3_Init();
  MX_SPI1_Init();
  MX_TIM2_Init();
  MX_FATFS_Init();
  MX_TIM4_Init();
  /* USER CODE BEGIN 2 */
#if defined(__TEST_MODE)
	EventRecorderInitialize(EventRecordAll, 1U);
	EventRecorderStart();
#endif
	SD_Inite();
	HAL_Delay(500);
	
	Kalman_Inite();
	Simple_FuzzyLogic_init();
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
	Waite_counter = 0;
	HAL_GPIO_WritePin(GPIOE, GPIO_PIN_1, GPIO_PIN_SET);
	ADC_Start();
	
  while (1)
  {
		if((ADCAcquisition_Flag || ADCHalfAcquisition_Flag) && !CircuitOpen_Flag)
		{
			/*ADC value conversion*/
			ADC_CurrentConversion(HALF_BUFFER_SIZE);

			if(!DetectBegin_Flag) 
			{
					if(MeasureBegin_Flag) /*measure begin*/
					{
						Kalman_Filter(HALF_BUFFER_SIZE);
						if(Waite_counter++ > 19) /*delay 0.1s after measure begin*/
						{
							DetectBegin_Flag = 1; /*detection begin*/
							Timer_Start();				/*start timer*/
						}
					}
					else MeasureBegin_Flag = Mesure_Begin(HALF_BUFFER_SIZE); /*exame voltage and current input*/
			}
			else if(ArcDetected_Flag) CircuitOpen_Flag = OpenConfirm(HALF_BUFFER_SIZE); 
			else
			{
				
#if defined(__TEST_MODE)
				EventStartA(0);
#endif
				/*kalman filter calculate*/
				Kalman_Filter(HALF_BUFFER_SIZE);
				/*arc detection with simple fuzzylogic*/				
				Simple_FuzzyLogic(HALF_DIFF_SIZE);
				
#if defined(__TEST_MODE)				
				EventStopA(0);
#endif
			}
			
			if(CircuitOpen_Flag)
			{
				/*stop acquisition*/
				ADC_Stop();
				
				/*Donwload resualt*/
				strcat(text, "sumFault: ");
				Value_byte2Char(&sl.SumFault, text);
				SD_Write(text);
				strcat(text, "SumMajFault: ");
				Value_byte2Char(&sl.SumMajFault, text);
				SD_Write(text);	
				strcat(text, "threshold: ");
				Value_float2Char(&sl.threshold, text);
				SD_Write(text);	
				strcat(text, "timespan: \n");
				Buffer_uint322Char(Symptom_timespan, text, FAULT_MAX_NUM);
				SD_Write(text);	
				strcat(text, "opendelay: \n");
				Val_uint322Char(openDelay, text);
				SD_Write(text);	
				strcat(text, "3rd order difference: \n");
				Buffer_float2Char(diff3_Buffer, text, HALF_DIFF_SIZE);
				SD_Write(text);
				strcat(text, "Residue: \n");
				Buffer_float2Char(Res_Buffer, text, HALF_BUFFER_SIZE);
				SD_Write(text);
				strcat(text, "Current: \n");
				Buffer_float2Char(Current_Buffer, text, HALF_BUFFER_SIZE);
				SD_Write(text);
				
				HAL_GPIO_WritePin(GPIOB, GPIO_PIN_0, GPIO_PIN_RESET);
			}
		}
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
  }
  /* USER CODE END 3 */
}

